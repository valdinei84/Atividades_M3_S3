# variável = porta nomes

calculadora = 0
numero1 = 10
numero2 = 4

# soma -> +
calculadora = numero1 + numero2
print(calculadora)
# subtração
calculadora = numero1 - numero2
print(calculadora)
# muliplicação
calculadora = numero1 * numero2
print(calculadora)
# potência
calculadora = numero1 ** numero2
print(calculadora)
# módulo
calculadora = numero1 % numero2
print(calculadora)