# Faça um Programa que calcule a área de um quadrado, 
# em seguida mostre o dobro desta área para o usuário.

y = float(input("Digite a metragem do lado direito de um quatrado: "))
x = float(input("Digite a metragem do lado esquerdo de um quatrado: "))

calculo = x * y
print("A área desse quadrado é de ", calculo, "metros quadrados")