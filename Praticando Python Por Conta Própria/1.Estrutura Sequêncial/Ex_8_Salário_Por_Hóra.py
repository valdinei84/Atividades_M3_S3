# Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês. 
# Calcule e mostre o total do seu salário no referido mês.

hora_trabalhada = float(input("Quanto voce ganha por hora trabalhada? Digita o bagulho ai: "))
Horas_Trabalhada = float(input("Quantas horas voce trabalha por mes: "))

Calculo = hora_trabalhada * Horas_Trabalhada
print("Voce vai receber ", Calculo, "neste mês")
